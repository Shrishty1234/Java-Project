package types.training;

public class VariableDemo {


    public static void main(String[] args) {

        //primitive 8 types
        //byte
        //short
        //int
        //long
        //double
        //char
        //boolean
        int age = 20;
        System.out.println(age);


        int list =30;
        System.out.println(list);


        byte myByte = 20;
        System.out.println(myByte);

        short myShort = 35; //[-32K to 32K]
        System.out.println(myShort);

        int myAge =8;
        System.out.println(myAge);


        long myLongNumber = 3_4_23_45L;
        System.out.println(myLongNumber);


        float dressPrice = 35.99F; //[max 6 digit]
        System.out.println(dressPrice);

        double shoePrice = 6.2345678902345234;// [max 15 digit]
        System.out.println(shoePrice);


        char myGender = 'F';
        System.out.println(myGender);


        boolean green =true;
        System.out.println(green);









    }




















}
